import React, { useState } from 'react';
import { FaFont, FaMicrophone, FaVolumeUp, FaSave, FaCog } from 'react-icons/fa';

const AccessibilityOptions = () => {
  const [settings, setSettings] = useState({
    fontSize: 'medium',
    voiceToText: false,
    textToSpeech: false,
    savePreset: false,
  });

  const handleSettingChange = (e) => {
    const { name, checked, value } = e.target;
    setSettings((prev) => ({
      ...prev,
      [name]: value || checked,
    }));
  };

  return (
    <div className="pt-20 w-full box-border"> {/* Adjust padding top as needed */}
      <section className="w-full h-screen bg-gray-100 p-6 lg:p-10">
        <div className="max-w-6xl mx-auto bg-white rounded-lg shadow-lg p-8">
          <h1 className="text-3xl font-extrabold mb-6 text-center">Accessibility Options</h1>

          {/* Section 1: Customizable Assessment Formats */}
          <div className="bg-blue-600 text-white rounded-lg shadow-lg p-6 mb-6">
            <h2 className="text-xl font-bold mb-4 flex items-center">
              <FaFont className="mr-2 text-2xl" /> Customizable Assessment Formats
            </h2>
            <div className="flex flex-col space-y-4">
              <label className="flex items-center">
                <input
                  type="radio"
                  name="fontSize"
                  value="small"
                  checked={settings.fontSize === 'small'}
                  onChange={handleSettingChange}
                  className="mr-2"
                />
                Small Font
              </label>
              <label className="flex items-center">
                <input
                  type="radio"
                  name="fontSize"
                  value="medium"
                  checked={settings.fontSize === 'medium'}
                  onChange={handleSettingChange}
                  className="mr-2"
                />
                Medium Font
              </label>
              <label className="flex items-center">
                <input
                  type="radio"
                  name="fontSize"
                  value="large"
                  checked={settings.fontSize === 'large'}
                  onChange={handleSettingChange}
                  className="mr-2"
                />
                Large Font
              </label>
            </div>
          </div>

          {/* Section 2: Alternative Input Methods */}
          <div className="bg-green-600 text-white rounded-lg shadow-lg p-6 mb-6">
            <h2 className="text-xl font-bold mb-4 flex items-center">
              <FaMicrophone className="mr-2 text-2xl" /> Alternative Input Methods
            </h2>
            <div className="flex flex-col space-y-4">
              <label className="flex items-center">
                <input
                  type="checkbox"
                  name="voiceToText"
                  checked={settings.voiceToText}
                  onChange={handleSettingChange}
                  className="mr-2"
                />
                Voice-to-Text
              </label>
              <label className="flex items-center">
                <input
                  type="checkbox"
                  name="textToSpeech"
                  checked={settings.textToSpeech}
                  onChange={handleSettingChange}
                  className="mr-2"
                />
                Text-to-Speech
              </label>
            </div>
          </div>

          {/* Section 3: Accessibility Presets */}
          <div className="bg-purple-600 text-white rounded-lg shadow-lg p-6 mb-6">
            <h2 className="text-xl font-bold mb-4 flex items-center">
              <FaSave className="mr-2 text-2xl" /> Accessibility Presets
            </h2>
            <p className="mb-4">
              Save your current settings as a preset for future assessments. This ensures that your preferred accessibility features are applied automatically.
            </p>
            <button
              onClick={() => setSettings((prev) => ({ ...prev, savePreset: !prev.savePreset }))}
              className={`bg-white text-purple-600 px-4 py-2 rounded-full shadow-lg hover:bg-purple-300 transition duration-300 ${
                settings.savePreset ? 'bg-purple-300' : ''
              }`}
            >
              {settings.savePreset ? 'Preset Saved' : 'Save Preset'}
            </button>
          </div>

          {/* Section 4: Stats and Analytics */}
          {/* <div className="bg-gray-300 text-gray-800 rounded-lg shadow-lg p-6">
            <h2 className="text-xl font-bold mb-4 flex items-center">
              <FaCog className="mr-2 text-2xl" /> Usage Statistics
            </h2>
            <p className="mb-4">
              Track your usage of accessibility features. This section provides insights into how often you use voice-to-text, text-to-speech, and other features.
            </p>
            <div className="grid grid-cols-1 md:grid-cols-2 gap-6">
              <div className="bg-gray-200 p-4 rounded-lg shadow-md">
                <h3 className="text-lg font-semibold mb-2">Voice-to-Text Usage</h3>
                <p>5 hours</p>
              </div>
              <div className="bg-gray-200 p-4 rounded-lg shadow-md">
                <h3 className="text-lg font-semibold mb-2">Text-to-Speech Usage</h3>
                <p>3 hours</p>
              </div>
            </div>
          </div> */}
        </div>
      </section>
    </div>
  );
};

export default AccessibilityOptions;
